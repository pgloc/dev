age = 35

if age < 2:
    print("Osoba jest niemowlęciem.")
elif age < 4:
    print("Osoba jest dzieckiem, które uczy się chodzić.")
elif age < 13:
    print("Osoba jest dzieckiem.")
elif age < 20:
    print("Osoba jest nastolatkiem.")
elif age < 65:
    print("Osoba jest dorosłym.")
else:
    print("Osoba jest seniorem.")